
public class User {
    private String name;
    String membership;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setMembership(String membership) {
        this.membership = membership;
    }

    public String getMembership() {
        return this.membership;
    }
}
