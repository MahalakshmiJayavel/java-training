

public class BitWiseOperator {
    public static void main(String[] args){
        int m= 7;
        int l= 9;

        System.out.println("m&l : "+ (m&l));
        System.out.println("m|l : "+ (m|l));
        System.out.println("m^l : "+ (m^l));
        System.out.println("~m : "+ (~m));



    }
}
