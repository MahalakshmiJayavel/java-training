public class UnaryOperators {
    public static void main (String[] args) {
        int number= 10;
        int number2= -6;
        int negativeNumber = -(number);//changes positive to negative as minus is already assigned
        int positiveNumber= -(number2);//changes negative to positive

        System.out.println("negative number: "+ negativeNumber);
        System.out.println("positive number: "+ positiveNumber);





    }

}
