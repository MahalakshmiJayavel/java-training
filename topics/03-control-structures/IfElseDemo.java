public class IfElseDemo {
    public static void main(String[] args) {
        int a = 50; // Input: score or value to check

        // Checking if 'a' is less than or equal to 40
        if (a <= 40) {
            System.out.println("Fail"); 
        } else {
            System.out.println("Pass"); 
        }
    }
}
