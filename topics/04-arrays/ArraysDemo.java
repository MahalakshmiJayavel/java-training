public class ArraysDemo {
    public static void main(String[] args) {
        // Declaring and initializing a String array with names
        String[] names = {"Maha", "Shamee", "Shwe"};

        // Printing the first element of the array (index 0)
        System.out.println("First name in the array: " + names[0]); // Output: Maha
    }
}
